#String Data Type
stype ="Start a day as a Idiot"
print(stype,type(stype))

#Integer Data Type
itype = 1995
print(itype,type(itype))

#Float Data Type
ftype = 90.0
print(ftype,type(ftype))

#Complex Data Type
ctype = 3j
print(ctype,type(ctype))

#List Data Type
list_type = ["Apple","Samsung","Huawei","Techno"]
print(list_type,type(list_type))

#Tuple Data Type
tu_type = ("BMW","Toyota","Chevrolet")
print(tu_type,type(tu_type))

#Range Data Type
range_data = range(13)
print(range_data,type(range_data))

#Dictionary Data Type
dic_type = {"name":"Tenet","genre":"Sci-Fi"}
print(dic_type,type(dic_type))

#Set Data Type
set_type = {"Tom","Jerry","Gary"}
print(set_type,type(set_type))

#FrozenSet Data Type
fset_type = frozenset({"Tom","Jerry","Gary"})
print(fset_type,type(fset_type))

#Boolean Data Type
boo_type = True
print(boo_type,type(boo_type))
